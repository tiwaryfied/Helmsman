def main() -> None:
    OUT.mkdir(parents=True, exist_ok=True)
    for name, body in [
        ("github_demo.yaml",   GITHUB_DEMO),
        ("linear_demo.yaml",   LINEAR_DEMO),
        ("slack_demo.yaml",    SLACK_DEMO),
        ("sentry_demo.yaml",   SENTRY_DEMO),
        ("datadog_demo.yaml",  DATADOG_DEMO),
        ("calendar_demo.yaml", CALENDAR_DEMO),
    ]:
        (OUT / name).write_text(body, encoding="utf-8")
        print(f"  wrote {(OUT / name).relative_to(ROOT)}")
    print(f"Done. Data URI base: {DATA_URI}")


if __name__ == "__main__":
    main()

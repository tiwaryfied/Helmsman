#!/usr/bin/env python3
"""
Generate Coral source spec YAMLs for Helmsman's Demo Mode.

Each source is `backend: file`, reads JSONL from ./data/<source>/, and exposes
schemas named `<source>_demo` (e.g. github_demo) so they don't collide with
the bundled live sources.

Output: ./coral-sources/*.yaml
Run:    python3 scripts/generate_source_specs.py
"""
from __future__ import annotations

from pathlib import Path
from urllib.parse import quote

ROOT = Path(__file__).resolve().parent.parent
DATA = ROOT / "data"
OUT = ROOT / "coral-sources"

# file:// URIs must URL-encode reserved characters (e.g. spaces).
DATA_URI = "file://" + quote(DATA.as_posix(), safe="/") + "/"

UTF8 = "Utf8"
INT = "Int64"
FLOAT = "Float64"
BOOL = "Boolean"
TS = "Timestamp"
JSON = "Json"


def render_table(name: str, description: str, sub_dir: str,
                 columns: list[tuple[str, str]], file_name: str | None = None) -> str:
    # Each table is pinned to its own JSONL file so a directory full of
    # tables doesn't cross-pollute schemas.
    fname = file_name or f"{name}.jsonl"
    lines = [
        f"  - name: {name}",
        f"    description: {description}",
        f"    format: jsonl",
        f"    source:",
        f"      location: {DATA_URI}{sub_dir}/",
        f'      glob: "{fname}"',
        f"    columns:",
    ]
    for col, typ in columns:
        lines.append(f"      - name: {col}")
        lines.append(f"        type: {typ}")
    return "\n".join(lines)


def render_source(schema: str, version: str, description: str,
                  test_queries: list[str], tables: list[str]) -> str:
    lines = [
        f"# Auto-generated by scripts/generate_source_specs.py",
        f"# Demo source for Helmsman -- backed by local JSONL under ./data/{schema.replace('_demo','')}/",
        f"name: {schema}",
        f"version: {version}",
        f"dsl_version: 3",
        f"backend: file",
        f"description: |",
        f"  {description}",
        f"test_queries:",
    ]
    for q in test_queries:
        # quote with single quotes; escape any single-quote in q
        safe = q.replace("'", "''")
        lines.append(f"  - '{safe}'")
    lines.append("tables:")
    for t in tables:
        lines.append(t)
    return "\n".join(lines) + "\n"


